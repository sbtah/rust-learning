// Every variable declared in RUST is immutable by default !!
// This decission was made to guarantee safe concurency of RUST
// Since all variables are immutable you don't have to worry on race conditions...

// Variables can be explicitly defined to allow mutation.
// Such variable is called a mutable.


// Immutable by default.
let variable_name = 1;


// Mutable variable.
let mut variable_name_2 = 2;


// Even tho we are allowed to change the value of a mutable variable.
// We cannot assign the value of anothet data type to it !


