// If you wish to create an array where every element is y and occours x number of times.
// You can do that with Rust with following syntax:

fn main() {
    let a = [10; 5];
    for i in a {
        print!("{i} ");
    }
    println!("");
}

// Create var a which will be an array of length 5 and each element in that array will be 10.